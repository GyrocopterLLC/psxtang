module fb_mem_control #(
    parameter int ADDR_WIDTH = 32,
    parameter int MEMORY_WIDTH = 128,
    parameter int PIXEL_SIZE = 24,
    parameter int BURST_SIZE = 16, // Number of pixels per burst
    parameter int MAX_LINE_WIDTH = 9, // Bits needed to describe the number of memory words per line
    parameter int MEM_INCREMENT = 1 // in counts of bursts (128bit/16byte)
)(
    input  logic                    rstn_i,

    // DDR3 memory interface
    input  logic                    mem_clk_i,
    input  logic                    mem_busy_i,
    output logic                    mem_wr_en_o,
    output logic                    mem_rd_en_o,
    output logic [ADDR_WIDTH-1:0]   mem_addr_o,
    output logic [MEMORY_WIDTH-1:0] mem_wr_data_o,
    input  logic [MEMORY_WIDTH-1:0] mem_rd_data_i,
    input  logic                    mem_rd_data_valid_i,

    // Video input interface
    input  logic                    vin_clk_i,
    input  logic [9:0]              vin_width_i,  // input image width (max: 1024)
    input  logic [8:0]              vin_height_i, // input image height (max: 512)
    input  logic                    vin_buf_wr_en_i, // Pixel ready to go to DDR3 FIFO
    input  logic [PIXEL_SIZE-1:0]   vin_buf_data_i, 
    input  logic [9:0]              vin_buf_start_pixel_i, // Pixel value at the start of most recent burst
    input  logic [9:0]              vin_buf_start_word_i,
    input  logic [8:0]              vin_buf_line_i, // Line for that same burst
    input  logic                    vin_buf_data_ready_i, // Burst has been filled 
    input  logic                    vin_buf_data_flush_i, // FIFO flush command
    input  logic [9:0]              vin_line_length_i, // Length of a line counted in memory words
    input  logic [1:0]              vin_frame_slot_i, // triple buffering uses 3 slots. Increment on Vsync

    // Video output interface
    input  logic                    vout_clk_i,
    input  logic                    vout_buf_data_req_i,
    input  logic [8:0]              vout_buf_rd_line_i,
    input  logic [9:0]              vout_buf_rd_pixel_i,
    input  logic [9:0]              vout_buf_rd_word_i, // Memory data word number for start of most recent burst
    output logic                    vout_buf_rd_complete_o, // asserted when burst read is complete
    input  logic                    vout_buf_rd_en_i,
    output logic [PIXEL_SIZE-1:0]   vout_buf_rd_data_o
);

